	public function dbAction()
	{
		$em = $this->getDoctrine()->getManager();

		$countries 		 = $this->data('countries'); //
		$cities 		 = $this->data('cities');    //
		$companies 		 = $this->data('companies'); //
		$branches 		 = $this->data('branches');  //
		$specializations = $this->data('specializations'); //

		foreach ($countries as $item) {
			$entity = new \OneDayJob\ApiBundle\Entity\Country();
			$entity->setTitle($item['title']);

			$em->persist($entity);
		}

		$em->flush();
		$repoCountry = $em->getRepository('OneDayJobApiBundle:Country');

		foreach ($cities as $item) {
			do {
                $country = $repoCountry->find(mt_rand(1, count($countries)));
            } while ($country == null);

			$entity = new \OneDayJob\ApiBundle\Entity\City();
			$entity->setTitle($item['title']);
			$entity->setCountry($country);

			$em->persist($entity);
		}

		$em->flush();
		$repoCity    = $em->getRepository('OneDayJobApiBundle:City');

		foreach ($companies as $item) {
			do {
                $country = $repoCountry->find(mt_rand(1, count($countries)));
            } while ($country->getCities() == null);

			$entity = new \OneDayJob\ApiBundle\Entity\Company();
			$entity->setName($item['name']);
			$entity->setSite($item['site']);
			$entity->setPhone($item['phone']);
			$entity->setDescription($item['description']);
			$entity->setCountry($country);
			$entity->setCity($country->getCities()[0]);

			$em->persist($entity);
		}

		$em->flush();

		foreach ($branches as $item) {
			$entity = new \OneDayJob\ApiBundle\Entity\Branch();
			$entity->setTitle($item['title']);

			$em->persist($entity);
		}

		$em->flush();
		$repoBranch  = $em->getRepository('OneDayJobApiBundle:Branch');

		foreach ($specializations as $item) {
			do {
                $branch = $repoBranch->find(mt_rand(1, count($branches)));
            } while ($branch == null);

			$entity = new \OneDayJob\ApiBundle\Entity\Specialization();
			$entity->setTitle($item['title']);
			$entity->setParent($branch);

			$em->persist($entity);
		}

		$em->flush();

		exit('OK');
	}

	public function db2Action()
	{
		$em = $this->getDoctrine()->getManager();
		$countries 		 = $this->data('countries'); //
		$cities 		 = $this->data('cities');    //
		$companies 		 = $this->data('companies'); //
		$branches 		 = $this->data('branches');  //
		$specializations = $this->data('specializations'); //

		$repoCompany = $em->getRepository('OneDayJobApiBundle:Company');
		$repoCountry = $em->getRepository('OneDayJobApiBundle:Country');
		$repoCity    = $em->getRepository('OneDayJobApiBundle:City');
		$repoBranch  = $em->getRepository('OneDayJobApiBundle:Branch');
		$repoSpecialization = $em->getRepository('OneDayJobApiBundle:Specialization');

		for ($i=1; $i <= count($companies); $i++) { 
			$_company = $repoCompany->find($i);

			if ($_company) {
				$temp = $_company->getCountry()->getCities()[0];
				$_company->setCity($temp);

				$em->persist($_company);
			}
		}

		$em->flush();


		foreach ($this->data('vacancies') as $item) {
			do {
                $company = $repoCompany->find(mt_rand(1, count($companies)));
            } while ($company == null);

            do {
                $country = $repoCountry->find(mt_rand(1, count($countries)));
            } while ($country->getCities() == null);

			$entity = new \OneDayJob\ApiBundle\Entity\Vacancy();
			$entity->setTitle($item['title']);
			$entity->setCompany($company);
			$entity->setCountry($country);
			
			do {
                $branch = $repoBranch->find(mt_rand(1, count($branches)));
            } while ($branch == null);

			$entity->setBranch($branch);
			$entity->setEmployment(0);
			$entity->setWorkSchedule(0);
			$entity->setWorkExperience(0);
			$entity->setEducation(0);

			$entity->setDuty($item['duty']);
			$entity->setSkill($item['skill']);
			$entity->setSalary($item['salary']);
			$entity->setCurrency('usd');
			$entity->setCreated(new \DateTime());

			$em->persist($entity);
		}

		$em->flush();

		exit('OK');
	}

	public function db3Action()
	{
